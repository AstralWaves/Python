print("=== Python Data Types Tutorial ===")

# NUMERIC TYPES: int, float, complex
# Handles numbers: whole, decimal, or complex.
print("\n=== int, float, complex ===")
num_int = 42  # Integer: whole number
num_float = 3.14  # Float: decimal number
num_complex = 2 + 3j  # Complex: real + imaginary
print(f"Integer: {num_int}, Add 10: {num_int + 10}")  # Output: 52
print(f"Float: {num_float}, Double: {num_float * 2}")  # Output: 6.28
print(f"Complex: {num_complex}")  # Output: (2+3j)